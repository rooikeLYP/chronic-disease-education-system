        <h2>一.什么是糖尿病</h2>
        <p class="text">糖尿病是由遗传和环境因素相互作用引起的一组以慢性高血糖为特征的代谢异常综合征。
          因胰岛素分泌和（或）作用缺陷而引起的碳水化合物、蛋白质、脂肪、水和电解质紊乱。
          随着病程的延长，可出现心、脑、肾、视网膜等多脏器损害，病情恶化时可出现酮症酸中毒、高血糖高渗状态等急性并发症。</p>
      </div>
      <div class="sweet-reason">
        <h2>二.糖尿病的病因有哪些</h2>
        <p class="text">糖尿病的病因至今尚未完全阐明，引起糖尿病的病因主要为遗传因素和环境因素两大类。</p>
        <h4>1.遗传因素</h4>
        <p class="sweet-reason-text">1型或2型糖尿病均存在明显的遗传异质性。糖尿病存在家族发病倾向,1/4～1/2患者有糖尿病家族史。</p>
