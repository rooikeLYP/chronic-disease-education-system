        <h4>2.环境因素</h4>
        <p class="text">1型糖尿病患者感染某些病毒如柯萨奇病毒，风疹病毒，腮腺病毒等后可启动自身免疫反应，导致胰岛素B细胞破坏。进食过多，体力活动不足导致的肥胖是2型糖尿病最主要的环境因素，使具有2型糖尿病遗传易感性的个体容易发病。</p>
        <img src="@/assets/img/disease/sweet/introduce/reason_4.jpg" alt="" class="img" style="height: 250px">
